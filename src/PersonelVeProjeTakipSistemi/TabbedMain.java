    private void buttonProjeDetaylariActionPerformed(java.awt.event.ActionEvent evt) {
        try{
            String txt;
            int selectedId = Integer.parseInt(devamEdenProjeTable.getValueAt(devamEdenProjeTable.getSelectedRow(), 0).toString());
            Proje tproje = dbc.getProjeler().get(selectedId);
            
            txt = "ID\t: " + selectedId + "\n" +
                    "Proje Adı\t: " + tproje.getProjeAdi()+ "\n" +
                    "Proje Sahibi\t: " + tproje.getProjeSahibi()+ "\n" +
                    "Proje Puanı\t: " + tproje.getProjePuani()+ "\n" +
                    "Başlangıç Tarihi\t: " + dateFormat.format(tproje.getProjeBaslangic()) + "\n" + 
                    "Bitiş Tarihi\t: " + dateFormat.format(tproje.getProjeBitis()) + "\n" +
                    "Gerekli Diller\t: " ;
            
            if(tproje.getProgDil(0))
                txt += "Assembly, ";
            if(tproje.getProgDil(1))
                txt += "Java, ";
            if(tproje.getProgDil(2))
                txt += "C/C++, ";
            if(tproje.getProgDil(3))
                txt += "C#, ";
            if(tproje.getProgDil(4))
                txt += "Javascript, ";
            if(tproje.getProgDil(5))
                txt += "Php, ";
            if(tproje.getProgDil(6))
                txt += "Html/Css, ";
            if(tproje.getProgDil(7))
                txt += "Python, ";
            txt = txt.substring(0, txt.lastIndexOf(",")) + "\n";
            
            txt += "Aktif Çalışanlar\t: ";
            
            for(Integer count:tproje.getAktifPersonel()){
                txt += dbc.getPersoneller().get(count).getAd() + " " + 
                       dbc.getPersoneller().get(count).getSoyad() + "\n\t  ";
            }
            
            projeTextArea.setText("");
            projeTextArea.setText(txt);
            dialogProjeDetayGoruntule.pack();
            dialogProjeDetayGoruntule.setLocationRelativeTo(null);
            dialogProjeDetayGoruntule.setVisible(true);
        
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lütfen proje seçiniz.");
        }
    }
    
