    private void projePersonelAtaButtonActionPerformed(java.awt.event.ActionEvent evt) {
        
        for(int i=0 ; i<devamEdenProjeTable.getRowCount() ; i++){
            uygunPersonellerTable.setValueAt("", i, 0);
            uygunPersonellerTable.setValueAt("", i, 1);
            uygunPersonellerTable.setValueAt("", i, 2);
            uygunPersonellerTable.setValueAt("", i, 3);
        }
        
        for(int i=0 ; i<calisanPersonellerTable.getRowCount() ; i++){
            calisanPersonellerTable.setValueAt("", i, 0);
            calisanPersonellerTable.setValueAt("", i, 1);
            calisanPersonellerTable.setValueAt("", i, 2);
            calisanPersonellerTable.setValueAt("", i, 3);
        }
        
        try{
            int selectedId = Integer.parseInt(devamEdenProjeTable.getValueAt(devamEdenProjeTable.getSelectedRow(), 0).toString());
            Proje tproje = dbc.getProjeler().get(selectedId);
            boolean yetenekKontrol;
            int sayac, uygunElemanSayisi = 0, calisanElemanSayisi = 0;
            String txt;
            for(Personel prs:dbc.getPersoneller()){
                yetenekKontrol = true; sayac = 0;
                if(!prs.getAktifProjeler().contains(selectedId)){
                    while(yetenekKontrol && sayac<8){
                        if(tproje.getProgDil(sayac)){
                            if(!prs.getProgDil(sayac)){
                                yetenekKontrol = false;
                            }
                        }
                        sayac++;
                    }
                    if(yetenekKontrol){
                        txt = prs.getPersonelID() + "";
                        uygunPersonellerTable.setValueAt(txt, uygunElemanSayisi, 0);
                        txt = prs.getAd() + " " + prs.getSoyad();
                        uygunPersonellerTable.setValueAt(txt, uygunElemanSayisi, 1);
                        uygunPersonellerTable.setValueAt(prs.getProgDiliString(), uygunElemanSayisi, 2);
                        uygunPersonellerTable.setValueAt(prs.getYabanciDilString(), uygunElemanSayisi, 3);
                        uygunElemanSayisi++;
                    }  
                }
            }
            
            for(Integer count:tproje.getAktifPersonel()){
                Personel prs = dbc.getPersoneller().get(count);
                txt = prs.getPersonelID() + "";
                calisanPersonellerTable.setValueAt(txt, calisanElemanSayisi, 0);
                txt = prs.getAd() + " " + prs.getSoyad();
                calisanPersonellerTable.setValueAt(txt, calisanElemanSayisi, 1);
                calisanPersonellerTable.setValueAt(prs.getProgDiliString(), calisanElemanSayisi, 2);
                calisanPersonellerTable.setValueAt(prs.getYabanciDilString(), calisanElemanSayisi, 3);  
                calisanElemanSayisi ++;
            }
            
            dialogPersonelAta.pack();
            dialogPersonelAta.setLocationRelativeTo(null);
            dialogPersonelAta.setTitle(dbc.getProjeler().get(selectedId).getProjeAdi() + " Projesine Personel Ata");
            dialogPersonelAta.setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Lütfen proje seçiniz.");
        }
       
    }
    
