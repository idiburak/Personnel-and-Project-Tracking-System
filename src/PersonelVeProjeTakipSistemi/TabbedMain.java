    // ANA PANELDE SECILEN SEKMENIN ICERIK GUNCELLEMESINI YAPAN METOD
    private void MainPaneStateChanged(javax.swing.event.ChangeEvent evt) {
        dbc.readPersoneller();
        if(MainPane.getSelectedIndex() == 0){
            int size = dbc.getPersoneller().size();
            String icerik = Integer.toString(size);
            fieldPersonelId.setText(icerik);
        }
        if(MainPane.getSelectedIndex() == 1){
            int row = 0;
            
            for(Personel prs:dbc.getPersoneller()){
                personelTable.getModel().setValueAt(prs.getPersonelID(), row, 0);
                personelTable.getModel().setValueAt(prs.getAd(), row, 1);
                personelTable.getModel().setValueAt(prs.getSoyad(), row, 2);
                personelTable.getModel().setValueAt(dateFormat.format(prs.getDogumTarihi()), row, 3);
                personelTable.getModel().setValueAt(prs.getCinsiyet(), row, 4);
                personelTable.getModel().setValueAt(prs.getKimlikNo(), row, 5);
                row++;
            }
        }
        if(MainPane.getSelectedIndex() == 2){
            int size = dbc.getProjeler().size();
            String icerik = Integer.toString(size);
            fieldProjeId.setText(icerik);
        }
        
        if(MainPane.getSelectedIndex() == 3){
            int row = 0;
            
            for(Proje prj:dbc.getProjeler()){
                if(prj.getProjeninBitmeDurumu()){
                    bitenProjeTable.getModel().setValueAt(prj.getProjeID(), row, 0);
                    bitenProjeTable.getModel().setValueAt(prj.getProjeAdi(), row, 1);
                    bitenProjeTable.getModel().setValueAt(dateFormat.format(prj.getProjeBaslangic()), row, 2);
                    bitenProjeTable.getModel().setValueAt(dateFormat.format(prj.getProjeBitis()), row, 3);
                    row++;
                }
            }
        }
        
        if(MainPane.getSelectedIndex() == 4){
            int row = 0;
            
            for(Proje prj:dbc.getProjeler()){
                if(!prj.getProjeninBitmeDurumu()){
                    devamEdenProjeTable.getModel().setValueAt(prj.getProjeID(), row, 0);
                    devamEdenProjeTable.getModel().setValueAt(prj.getProjeAdi(), row, 1);
                    devamEdenProjeTable.getModel().setValueAt(dateFormat.format(prj.getProjeBaslangic()), row, 2);
                    devamEdenProjeTable.getModel().setValueAt(dateFormat.format(prj.getProjeBitis()), row, 3);
                    row++;
                }
            }
        }
        
    }
    
