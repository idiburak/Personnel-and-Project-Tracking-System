    //SECILEN PROJEYE BITIS TARIHI ATAYAN METOD
    private void projeTarihBelirleButtonActionPerformed(java.awt.event.ActionEvent evt) {
        try{
            int selectedId = Integer.parseInt(devamEdenProjeTable.getValueAt(devamEdenProjeTable.getSelectedRow(), 0).toString());
            Proje tproje = dbc.getProjeler().get(selectedId);
            tproje.setProjeninBitmeDurumu(true);
            tproje.setProjeBitis(calendarTarihBelirle.getDate());
            dbc.getProjeler().set(selectedId, tproje);
            dbc.writeProjeler();
            JOptionPane.showMessageDialog(null,"Proje tamamlandı.");
            
            for(int i=0 ; i<devamEdenProjeTable.getRowCount() ; i++){
                devamEdenProjeTable.getModel().setValueAt("", i, 0);
                devamEdenProjeTable.getModel().setValueAt("", i, 1);
                devamEdenProjeTable.getModel().setValueAt("", i, 2);
                devamEdenProjeTable.getModel().setValueAt("", i, 3);
            }
            MainPane.setSelectedIndex(3);
            
            for(Integer count:tproje.getAktifPersonel()){
                dbc.getPersoneller().get(count).aktifProjeSil(selectedId);
            }
            
            dbc.writeProjeler();
            dbc.writePersoneller();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Lütfen tarih seçiniz.");
        }
        projeTarihBelirleme.dispose();
    }
    
